#include "21sh.h"

void    ft_free_arr(char **arr)
{
    int i;

    if (arr)
    {
        i = 0;
        while (arr[i])
        {
            ft_strdel(&arr[i]);
            i++;
        }
        if (arr)
            arr = NULL;
    }
}